package com.tesoem.g7s21p6hgle;

public class Datos {

    String nombre;
    String comidaFavorita;
    String colorFavorito;
    String numFavorito;


    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getComidaFavorita() {
        return comidaFavorita;
    }

    public void setComidaFavorita(String comidaFavorita) {
        this.comidaFavorita = comidaFavorita;
    }

    public String getColorFavorito() {
        return colorFavorito;
    }

    public void setColorFavorito(String colorFavorito) {
        this.colorFavorito = colorFavorito;
    }

    public String getNumFavorito() {
        return numFavorito;
    }

    public void setNumFavorito(String numFavorito) {
        this.numFavorito = numFavorito;
    }
}
